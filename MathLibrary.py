# Day 12: Using the math library
import math

# AI models often use logarithmic functions for error calculation (Cross-Entropy Loss)
value = 2.71828 

# Calculating natural logarithm and square root
log_val = math.log(value)
sqrt_val = math.sqrt(144)

print(f"Natural Log of {value} is approx: {log_val:.2f}")
print(f"Square root of 144 is: {int(sqrt_val)}")